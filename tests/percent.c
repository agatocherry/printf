#include <stdio.h>
#include "../includes/printf.h"

int	main(void)
{
	int	i;

	i = -100;
	PRINT("1 [%%]\n");
	PRINT("2 [%05%]\n");
	while (i < 100)
	{
		PRINT("4 [%0*%]\n", i);
		i++;
	}
	PRINT("5 [%0%]\n");
	PRINT("6 [%-5%]\n");
	i = -100;
	while (i < 100)
	{
		PRINT("7 [%-*%]\n", i);
		i++;
	}
	PRINT("8 [%-%]\n");
	PRINT("9 [%.3%]\n");
	i = -100;
	while (i < 100)
	{
		PRINT("10 [%.*%]\n", i);
		i++;
	}
	PRINT("11 [%.%]\n");
	PRINT("12 [%.0%]\n");
	PRINT("13 [%-05%]\n");
	PRINT("14 [%-055%]\n");
	PRINT("15 [%0-5%]\n");
	PRINT("16 [%0-55%]\n");
	PRINT("17 [%0.5%]\n");
	PRINT("18 [%0.55%]\n");
	PRINT("19 [%0*.*%]\n", -5, -10);
	PRINT("20 [%*.*%]\n", -5, -10);
	PRINT("21 [%0*.*%]\n", 5, 10);
	PRINT("22 [%*.*%]\n", 5, 10);
	PRINT("23 [%10.%]\n");
	PRINT("24 [%.1%]\n");
	PRINT("25 [%1%]\n");
	PRINT("26 [%.5%]\n");
	PRINT("27 [%%%%%%]\n");
	PRINT("28 [%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%]\n");
	PRINT("29 [%-999999%]\n");
	PRINT("30 [%0999999%]\n");
	return (0);
}
