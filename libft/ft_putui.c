#include "libft.h"

void	ft_putui(unsigned int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n > 9)
	{
		ft_putui(n / 10);
	}
	ft_putchar('0' + n % 10);
}
